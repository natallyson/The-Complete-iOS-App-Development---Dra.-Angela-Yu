func add(a: Int, b: Int){
    let resultado = a + b
    print("\(a) + \(b) = \(resultado)")
}

func subtract(a: Int, b: Int){
    let resultado = a - b
    print("\(a) - \(b) = \(resultado)")
}

func multiply(a: Int, b: Int){
    let resultado = a * b
    print("\(a) * \(b) = \(resultado)")
}

func divide(a: Int, b: Int){
    let resultado = a / b
    print("\(a) / \(b) = \(resultado)")
}
